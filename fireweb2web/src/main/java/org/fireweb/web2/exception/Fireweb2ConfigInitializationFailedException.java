package org.fireweb.web2.exception;

public class Fireweb2ConfigInitializationFailedException extends Fireweb2Exception {

	public Fireweb2ConfigInitializationFailedException() {
		super();
	}

	public Fireweb2ConfigInitializationFailedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public Fireweb2ConfigInitializationFailedException(String message, Throwable cause) {
		super(message, cause);
	}

	public Fireweb2ConfigInitializationFailedException(String message) {
		super(message);
	}

	public Fireweb2ConfigInitializationFailedException(Throwable cause) {
		super(cause);
	}

}
