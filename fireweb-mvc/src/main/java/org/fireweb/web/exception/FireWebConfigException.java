package org.fireweb.web.exception;

public class FireWebConfigException extends FireWebException {

	public FireWebConfigException() {
		super();
	}

	public FireWebConfigException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FireWebConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public FireWebConfigException(String message) {
		super(message);
	}

	public FireWebConfigException(Throwable cause) {
		super(cause);
	}

}
